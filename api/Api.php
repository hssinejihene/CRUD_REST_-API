<?php

//API Class: insert, update,delete

class API
{
	private $connect = '';

	function __construct()
	{
		$this->database_connection();
	}

	function database_connection()
	{
		$this->connect = new PDO("mysql:host=localhost;dbname=tp1", "root", "");
	}

	function fetch_all()
	{
		$query = "SELECT * FROM carte ORDER BY id";
		$statement = $this->connect->prepare($query);
		if($statement->execute())
		{
			while($row = $statement->fetch(PDO::FETCH_ASSOC))
			{
				$data[] = $row;
			}
			return $data;
		}
	}

	function insert()
	{
		if(isset($_POST["nume"]))
		{
			$form_data = array(
				':nume'		=>	$_POST["nume"],
				':mdp'		=>	$_POST["mdp"]
			);
			$query = "
			INSERT INTO carte 
			(num, pwd) VALUES 
			(:nume, :mdp)
			";
			$statement = $this->connect->prepare($query);
			if($statement->execute($form_data))
			{
				$data[] = array(
					'success'	=>	'1'
				);
			}
			else
			{
				$data[] = array(
					'success'	=>	'0'
				);
			}
		}
		else
		{
			$data[] = array(
				'success'	=>	'0'
			);
		}
		return $data;
	}

	function fetch_single($id)
	{
		$query = "SELECT * FROM carte WHERE id='".$id."'";
		$statement = $this->connect->prepare($query);
		if($statement->execute())
		{
			foreach($statement->fetchAll() as $row)
			{
				$data['nume'] = $row['num'];
				$data['mdp'] = $row['pwd'];
			}
			return $data;
		}
	}

	function update()
	{
		if(isset($_POST["nume"]))
		{
			$form_data = array(
				':nume'	=>	$_POST['nume'],
				':mdp'	=>	$_POST['mdp'],
				':id'			=>	$_POST['id']
			);
			$query = "
			UPDATE carte 
			SET num = :nume, pwd = :mdp 
			WHERE id = :id
			";
			$statement = $this->connect->prepare($query);
			if($statement->execute($form_data))
			{
				$data[] = array(
					'success'	=>	'1'
				);
			}
			else
			{
				$data[] = array(
					'success'	=>	'0'
				);
			}
		}
		else
		{
			$data[] = array(
				'success'	=>	'0'
			);
		}
		return $data;
	}
	function delete($id)
	{
		$query = "DELETE FROM carte WHERE id = '".$id."'";
		$statement = $this->connect->prepare($query);
		if($statement->execute())
		{
			$data[] = array(
				'success'	=>	'1'
			);
		}
		else
		{
			$data[] = array(
				'success'	=>	'0'
			);
		}
		return $data;
	}
}

?>